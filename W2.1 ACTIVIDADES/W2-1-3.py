# Descripción del ejercicio: Haz un programa que pida dos palabras, si las dos palabras son iguales que imprima "Las dos palabras son iguales" y si no lo son que iumprima "Las dos palabaras son distintas"

palabra1 = input("Introduce una palabra: ")
palabra2 = input("Introduce otra palabra: ")

