# Descripción del ejercicio: Haz un programa que lea un caráter, si el carácter es A que imprima "El carácter es A" y si no que imprima "El carácter no es A"


caracter = input("Introduce un carácter: ")

if (caracter == "A"):
    print("El carácter es A")
else:
    print("El carácter no es A")