# Descripción del ejercicio: Escribir un programa que pida al usuario un número entero y si es mayor que 5 que imprima "El número es mayor que 5".

numero = int(input("Introduce un número: "))

if (numero > 5):
    print("El número es mayor que 5")

print("Fin del programa")